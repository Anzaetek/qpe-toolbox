import time

import numpy as np

from .hadamard_test import run_hadamard_test


def robust_phase_estimation(
    H, psi0, epsilon, sign_E0, n_steps, n_shots, *, trotter_order=2, verbosity=0
):
    r"""
    Perform the Robust Phase Estimation (RPE) algorithm.

    This routine estimates the phase associated with the unitary
    time evolution generated by a Hamiltonian using a sequence of
    Hadamard tests at increasing evolution times.

    The algorithm achieves a target precision ``epsilon`` by iteratively
    refining the phase estimate using powers of two evolution times.

    Parameters
    ----------
    H : Hamiltonian
        Hamiltonian object from the ``Hamiltonian`` class.
    psi0 : quimb.tensor.MatrixProductState
        Initial quantum state :math:`\ket{\psi_0}` of the system.
    epsilon : float
        Target precision for the phase estimate.
    sign_E0 : float
        Sign of the target energy eigenvalue.
    n_steps : int
        Number of Trotter steps used to approximate the time evolution.
        If zero, exact time evolution is used.
    n_shots : int, default ``0``
        Number of measurement shots used in the Hadamard test.
        If zero, probabilities are computed exactly.
    trotter_order : int, default ``2``
        Order of the Trotter-Suzuki decomposition.
    verbosity : int, default ``0``
        Verbosity level. If >= 1, print intermediate phase estimates.

    Returns
    -------
    theta_list : list of float
        List of phase estimates at each iteration.
        The last element corresponds to the most accurate estimate.

    """
    if abs(abs(sign_E0) - 1.0) > 1e-12:
        raise ValueError("sign_E0 must be +-1")

    st = time.time()
    M = int(np.ceil(np.log2(1 / epsilon)))

    theta_list = [0]
    if verbosity >= 1:
        print(f"m \t {'phi_m':<6} \t {'theta_m':<6} \t {'time (s)'}")
    for m in range(M + 1):
        if n_steps == "exact":
            phi_m = rpe_get_hadamard_output(H, psi0, m, n_steps, n_shots)
        else:
            phi_m = rpe_get_hadamard_output(
                H, psi0, m, n_steps * 2**m, n_shots, trotter_order=trotter_order
            )

        if m == 0:
            theta_m = phi_m
        else:
            S_m = [(phi_m + sign_E0 * 2 * np.pi * k) / 2**m for k in range(2**m)]
            theta_m, _d_min = rpe_update_theta(S_m, theta_list[m])

        if verbosity >= 1:
            et = time.time() - st
            print(
                f"{m} \t {np.round(phi_m, 4):<6} \t {np.round(theta_m, 4):<6} \t {et:<6.1f}"
            )

        theta_list.append(theta_m)

    return theta_list


def rpe_get_hadamard_output(H, psi0, m, n_steps, n_shots, *, trotter_order=2):
    r"""
    Estimate the phase of :math:`\bra{\psi_0}\exp(-i H 2^m)\ket{\psi_0}` using Hadamard tests.

    This function computes the phase corresponding to the unitary
    evolution over time :math:`2^m` by evaluating real and imaginary parts
    via Hadamard tests.

    Parameters
    ----------
    H : Hamiltonian
        Hamiltonian object defining the system.
    psi0 : quimb.tensor.MatrixProductState
        Initial quantum state :math:`\ket{\psi_0}`.
    m : int
        Iteration index corresponding to evolution time ``2**m``.
    n_steps : int
        Number of Trotter steps.
        If zero, exact time evolution is used.
    n_shots : int, default ``0``
        Number of measurement shots used in the Hadamard test.
        If zero, probabilities are computed exactly.
    trotter_order : int, default ``2``
        Order of the Trotter-Suzuki decomposition.

    Returns
    -------
    phi_m : float
        Estimated phase angle in radians.

    """
    n_qubits = H.n_qubits
    phys_reg = list(range(1, n_qubits + 1))
    t = 2**m
    if (n_steps == "exact") or (n_steps == 0):
        U_m = H.get_U_exact(t, phys_reg, controls=(0,))
    else:
        if not (n_steps > 0):
            raise ValueError("Can only evolve for positive n_steps")
        dt = t / n_steps
        U_m = [H.get_trotter_step(dt, phys_reg, trotter_order)] * n_steps
    X_m = run_hadamard_test(psi0, U_m, 0, n_shots)
    Y_m = run_hadamard_test(psi0, U_m, -np.pi / 2, n_shots)
    Z_m = X_m + 1j * Y_m
    return -np.angle(Z_m)


def rpe_distance(phi, theta):
    """
    Compute the angular distance between two angles.

    The distance is defined modulo π and lies in the interval [0, π].

    Parameters
    ----------
    phi : float
        First angle in radians.
    theta : float
        Second angle in radians.

    Returns
    -------
    d : float
        Angular distance between ``phi`` and ``theta``.

    """
    sign = np.sign(phi - theta)
    K = int(abs(phi - theta) / (2 * np.pi) + 1 / 2)
    return abs(phi - theta - K * sign * 2 * np.pi)


def rpe_update_theta(S, theta_ref):
    """
    Find the angle in a set closest to a reference angle.

    The comparison is performed using the angular distance
    modulo 2π.

    Parameters
    ----------
    S : list of float
        Candidate angles.
    theta_ref : float
        Reference angle.

    Returns
    -------
    theta_min : float
        Angle in ``S`` closest to ``theta_ref``.
    d_min : float
        Corresponding minimal angular distance.

    """
    d_min = rpe_distance(S[0], theta_ref)
    theta_min = S[0]
    for theta in S[1:]:
        if (rpe_distance(theta, theta_ref) <= d_min) and (-np.pi <= theta < np.pi):
            d_min = rpe_distance(theta, theta_ref)
            theta_min = theta
    return theta_min, d_min
